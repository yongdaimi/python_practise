# title() 函数，用于将字符串中每个单词的首字母转换成大写，注意：只将单词的首字母转换成大写，其它仍然是小写

str = "math, Phy, english"
str1 = str.title()
print(str1) # 输出： Math, Phy, English


# low() 函数，将所有字母转换成小写
str = "Storm Sprint, OG, Topson, NOTAIL"
str1 = str.lower()
print(str1) # 输出：storm sprint, og, topson, notail

# upper()函数，将所有字母转换成大写
str = "ana, maybe, ame, topson"
str1 = str.upper()
print(str1) # 输出：ANA, MAYBE, AME, TOPSON



