
# 定义一个函数用于做累加操作，分别演示值传递和引用传递的区别

def plusplus(obj):
    obj += obj;
    print("形参的值: ", obj)

# 字符串
a = "abc"
plusplus(a)
print("实参的值: ", a)


# list
print("------------------------------- list --------------------------------")
a = [1, 2, 3, 4, 5]
plusplus(a)
print("实参的值: ", a)

'''
可以看到，对不可变类型(字符串)做累加操作，不会修改原有的值；
对可变类型(列表)做累加操作，会修改原有的值。
'''